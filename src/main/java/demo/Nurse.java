package demo;

public class Nurse implements Staff{
    private int id ;
    private String name;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public void assist(){
        System.out.println("Nurse is assisting the doctor");
    }
}
